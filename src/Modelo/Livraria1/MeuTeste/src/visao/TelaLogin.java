    private void jButtonAcessoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcessoActionPerformed
        if(jTextFieldUsuario.getText().equals("admin")&&jPasswordFieldSenha.getText().equals("admin")){
        
        TelaPrincipal tela = new TelaPrincipal();
        tela.setVisible(true);
        dispose();
          }
        
        else if(jTextFieldUsuario.getText().equals("") || jPasswordFieldSenha.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
        else {
            int login = 0;
            for(Usuario e: lista.getGrupo()){
                if(e instanceof Cliente && ((Cliente) e).getNome().equals(jTextFieldUsuario.getText()) && ((Cliente) e).getSenha().equals(jPasswordFieldSenha.getText())){
                    TelaCliente tela = new TelaCliente();
                    tela.setVisible(true);
                    login=1;
                    dispose();
                }else
                if(e instanceof Admin && ((Admin) e).getNome().equals(jTextFieldUsuario.getText()) && ((Admin) e).getSenha().equals(jPasswordFieldSenha.getText())){
                    TelaPrincipal tela = new TelaPrincipal();
                    tela.setVisible(true);
                    login = 1;
                    dispose();
                }
            }
            if(login == 0){
                JOptionPane.showMessageDialog(null, "Dados Inv치lidos");
                jPasswordFieldSenha.setText("");
                jTextFieldUsuario.setText("");
            }
                
            
        }
        

    }//GEN-LAST:event_jButtonAcessoActionPerformed

